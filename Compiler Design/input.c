#include <stdio.h> // This is a header file for standard input-output
int main() {
    // This is a sample C program to calculate the sum of two integers.
    int num1 = 10; // The first number
    int num2 = 20; // The second number

    /* Calculate the sum */
    int sum = num1 + num2; // Store the sum in the 'sum' variable

    // Print the result
    printf("The sum is: %d\n", sum); // Output the sum
    return 0;
}

